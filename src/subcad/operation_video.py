def _mesh_bbox(mesh: Any) -> dict:
    bounds = getattr(mesh, "bounds", None)
    if bounds is None:
        return {
            "x_min": 0.0, "x_max": 0.0,
            "y_min": 0.0, "y_max": 0.0,
            "z_min": 0.0, "z_max": 0.0,
        }
    bmin = bounds[0]
    bmax = bounds[1]
    return {
        "x_min": float(bmin[0]),
        "x_max": float(bmax[0]),
        "y_min": float(bmin[1]),
        "y_max": float(bmax[1]),
        "z_min": float(bmin[2]),
        "z_max": float(bmax[2]),
        "length": float(bmax[0] - bmin[0]),
        "width": float(bmax[1] - bmin[1]),
        "height": float(bmax[2] - bmin[2]),
    }


def _distance(a: list[float], b: list[float]) -> float:
    return math.dist([float(v) for v in a[:3]], [float(v) for v in b[:3]])


def _as_float(value: Any) -> float | None:
    try:
        if value is None:
            return None
        result = float(value)
        if math.isfinite(result):
            return result
    except (TypeError, ValueError):
        return None
    return None


def _round_float(value: Any, digits: int = 6) -> float | None:
    numeric = _as_float(value)
    if numeric is None:
        return None
    return round(numeric, digits)


def _time_slug(time_sec: float) -> str:
    return f"{max(float(time_sec), 0.0):010.3f}".replace(".", "_")


def _write_json(path: Path, data: Any) -> None:
    with path.open("w", encoding="utf-8") as fh:
        json.dump(_json_ready(data), fh, indent=2)


def _json_ready(value: Any) -> Any:
    if isinstance(value, dict):
        return {str(k): _json_ready(v) for k, v in value.items()}
    if isinstance(value, (list, tuple)):
        return [_json_ready(v) for v in value]
    if hasattr(value, "tolist"):
        return value.tolist()
    return value


