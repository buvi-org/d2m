def _plan_uses_turning(plan: dict) -> bool:
    operations = plan.get("operations", []) if isinstance(plan, dict) else []
    for op in operations:
        text = " ".join(
            str(op.get(key) or "")
            for key in ("operation", "process", "tool_type", "strategy")
            if isinstance(op, dict)
        ).lower()
        if "turn" in text or "lathe" in text or "boring" in text:
            return True
    return False


